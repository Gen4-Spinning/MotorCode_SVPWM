/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_COMP1_Init();
  MX_DAC3_Init();
  MX_FDCAN1_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM17_Init();
  MX_USART3_UART_Init();
  MX_TIM7_Init();
  /* USER CODE BEGIN 2 */

  HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, GPIO_PIN_SET);
  //SET UP ABI mode.
  SetupABIwithoutPWM();
  settings1Reg.raw = AS5047_readRegister(SETTINGS1_REG,0);
  settings2Reg.raw = AS5047_readRegister(SETTINGS2_REG,0);
  abiSettingsOK = Check_ABI_SetCorrectly(settings1Reg,settings2Reg);

  uint16_t zeroValue = 1175;
  zeroPos = getProgrammedZeroOffset();
  zeroRegisterUpdated = 0;
  if (zeroPos != zeroValue){
	writeZeroReg(zeroValue); //function must check if it got back the same value it wrote.
	zeroRegisterUpdated = 1;
	zeroPos = getProgrammedZeroOffset();
	//to check if this value is same as what we wrote
  }

  //start the encoder
  HAL_TIM_Encoder_Start_IT(&htim2,TIM_CHANNEL_ALL);
  //get the current angle and set it in the timer CNT register.
  startingAngle16Bit = GetAveragedAngleReading(10);  // take the average of 10 readings and then take that as the starting angle.
  startingAngleMech = startingAngle16Bit * SPI_RDNG_TO_MECH_ANGLE;
  startingCNT_val = (int)((startingAngle16Bit * SPI_RDNG_TO_ENC_CNT)+0.5f); // rounded
  __HAL_TIM_SET_COUNTER(&htim2,startingCNT_val);

  //SETUP SVPWM
  initSVPWM(&svpwmObj);
  init_RtrSpdPos(&rtrSpdPosObj,startingAngle16Bit); // set the rotor to the current rotor position

  //HAL_TIM_Base_Start_IT(&htim7); // 1 ms interrupt for speed calculation

  //Start PWM

  HAL_TIM_Base_Start_IT(&htim1);
  HAL_TIM_PWM_Start_IT(&htim1,TIM_CHANNEL_1);
  HAL_TIMEx_PWMN_Start_IT(&htim1,TIM_CHANNEL_1);

  HAL_TIM_PWM_Start_IT(&htim1,TIM_CHANNEL_2);
  HAL_TIMEx_PWMN_Start_IT(&htim1,TIM_CHANNEL_2);

  HAL_TIM_PWM_Start_IT(&htim1,TIM_CHANNEL_3);
  HAL_TIMEx_PWMN_Start_IT(&htim1,TIM_CHANNEL_3);

  htim1.Instance->CCR1 = 0; // highest value is 1499
  htim1.Instance->CCR2 = 0; // highest value is 1499
  htim1.Instance->CCR3 = 0; // highest value is 1499


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  HAL_GPIO_TogglePin(GPIOC,LED1_Pin);
	  HAL_Delay(100);
	  HAL_GPIO_TogglePin(GPIOC,LED2_Pin);
	  HAL_Delay(100);
	  HAL_GPIO_TogglePin(GPIOC,LED3_Pin);
	  HAL_Delay(100);
	  //motorStart = 1;

	  rtrSpdPosObj.MSpd_RPM = target;
	  update_RtrSpd(&rtrSpdPosObj); // goes later into a  1ms timer

	  //angleData =  AS5047_readRegister(ANGLE_REG,0)//TODO why does this fn not work?;

	  /* To check if the ABI connections are correct, read both the SPI and the Enc counts and make sure they are very close
   	   to each other and increase in the same direction! */
	  encoderCNT = htim2.Instance->CNT;
	  angleMech_encCnt = encoderCNT * ENC_CNT_TO_MECH_ANGLE;

	  angleData = angleReading();
	  angleMech = angleData*360.0/16384;


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */
}

